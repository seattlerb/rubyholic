#   end

  def util_new target, type, *args
    plural = Inflector.pluralize type.to_s
    stuff = target.send(plural)[0..-1]
    thing = target.send("new_#{type}", *args)
    new_stuff = target.send(plural) - stuff
    assert_equal [thing], new_stuff
    return thing
  end
end
